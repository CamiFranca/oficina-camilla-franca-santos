class Calculadora {
    soma(a, b) {
    const soma = a + b;
    return soma;
  }

  subtrair(a,b){
    const subtrai = a - b
    return subtrai
  }

  multiplicar(a, b){
    const multiplica = a * b
    return multiplica
  }

  dividir(a, b){
    const dividi = a / b
    return dividi
  }
} 

module.exports = Calculadora